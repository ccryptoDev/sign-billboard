        var login = $('#kt_login');
        var showErrorMsg = function(form, type, msg) {
            var alert = $('<div class="alert alert-' + type + ' alert-dismissible" role="alert">\
                <div class="alert-text">'+msg+'</div>\
                <div class="alert-close">\
                    <i class="flaticon2-cross kt-icon-sm" data-dismiss="alert"></i>\
                </div>\
            </div>');

            form.find('.alert').remove();
            alert.prependTo(form);
            //alert.animateClass('fadeIn animated');
            KTUtil.animateClass(alert[0], 'fadeIn animated');
            alert.find('span').html(msg);
        }
		
		$("#forget_form").submit(function(event){
            event.preventDefault();
            var fs = new FormData(document.getElementById('forget_form'));
            $("#kt_login_forgot_submit").addClass('kt-spinner kt-spinner--right kt-spinner--sm kt-spinner--light').attr('disabled', true);
            $.ajax({
                url : '/forget',
                type : "POST",
                contentType : false,
                processData : false,
                data : fs,
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                },
                success : function(response, status, xhr, $form){
                    $("#kt_login_forgot_submit").removeClass('kt-spinner kt-spinner--right kt-spinner--sm kt-spinner--light').attr('disabled', false);
                    if(response == "success"){
                        showErrorMsg($("#forget_form"), 'success', 'Success. Please check your email.');
                    }
                    else{
                        showErrorMsg($("#forget_form"), 'danger', 'Incorrect username or password. Please try again.');
                    }
                },
				error : function(res){
					location.reload()
				}
            })
        })
        $("#login-form").submit(function(event){
            event.preventDefault();
            var fs = new FormData(document.getElementById('login-form'));
            $("#kt_login_signin_submit").addClass('kt-spinner kt-spinner--right kt-spinner--sm kt-spinner--light').attr('disabled', true);
            $.ajax({
                url : '/login',
                type : "POST",
                contentType : false,
                processData : false,
                data : fs,
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                },
                success : function(response, status, xhr, $form){
                    $("#kt_login_signin_submit").removeClass('kt-spinner kt-spinner--right kt-spinner--sm kt-spinner--light').attr('disabled', false);
					if(response == "Your account is Inactivated"){
						showErrorMsg($("#login-form"), 'danger',"Your account is currently inactive.  Please contact us at support@inex.net or call (405)415-3002");
					}
                    else if(response == "fail"){
                        showErrorMsg($("#login-form"), 'danger', 'Incorrect username or password. Please try again.');
					}
					else{
						document.location = response;
					}
				},
				error : function(res){
					location.reload()
				}
            })
        })
