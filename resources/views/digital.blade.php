                                    <span class="bottom25"><a>Outdoor / Indoor: Why designate whether a sign is Outdoor or Indoor? It changes the type of cabinet that the manufacturer uses. Manufacturers like to classify Outdoor cabinets as IP65 while Indoor are normally IP30 to IP50.  The first number indicates dust resistance, the second is water resistance. For example, IP44 indicates dust resistant, splash resistant, while IP65 normally means dust-proof and wash-down capable.  The manufacturers’ designations are not actually correct.   Dust gets into the Outdoor cabinets.  They are not actually sealed against that issue.  The cabinets are water resistant, not waterproof.  It is possible for water to get inside, though not highly likely.  In any case, your biggest enemy for Outdoor or Indoor signs are dust and condensation from humidity. 
