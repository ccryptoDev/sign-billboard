                            <p class="top15">
                                We cannot emphasize this point enough.   The text size controls the screen size.   You do NOT choose the sign size and then fit the text in it.  You figure out the required text size and then ‘wrap’ the size of the sign around it. <br>
                                The primary purpose of an effective sign is readability. From years of study, it has been determined that the recommended minimum reading time is 3-5 seconds.  What size letter will your sign require to allow for sufficient readability of your message before you have driven passed it?  <br>
                            </p>
                            <p class="top15">
                                The right size means the text must be legible --- at the right distance from the sign.  For text to legible, the font must be 1 inch high for every 10 feet away the viewer stands. Consider this point: If you bought a 2 ft high x 6 ft wide display, and you need text that is 10 inches tall, the text takes up 40% of the sign’s entire height.  Hmm.   That sign is too small.   Get the point? 
                            </p>

